// ‡¶´‡¶æ‡¶Ç‡¶∂‡¶® ‡¶ï‡¶≤ addBorder(["abc", "def", "ghi"]
// ‡¶Ü‡¶â‡¶ü‡¶™‡ßÅ‡¶ü [ '*****', '*abc*', '*def*', '*ghi*', '*****' ]

function addBorder(picture) {
  const wall = "*".repeat(picture[0].length + 2);

  picture.unshift(wall);
  picture.push(wall);

  for (let i = 1; i < picture.length - 1; i++) {
    picture[i] = `*${picture[i]}*`;
  }
  return picture;
}

